package com.company.Operators.ForOperator;

public class Task_02 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {

            for (int a = 1; a <= 10; a++) {
                System.out.print(a * i + " ");
            }
            System.out.println();

        }
    }
}
